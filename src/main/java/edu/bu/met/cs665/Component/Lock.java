package edu.bu.met.cs665.Component;

/**
 * This class represents a Lock for the washer lid 
 * 
 * @author Sahad Osman
 *
 */
public class Lock{
	

	/**
	 * This method close lid
	 */
	public void off() {
		System.out.println("The lid is closed");
		
	}
	
	/**
	 * This method open lid
	 */
	public void on() {
		System.out.println("The lid is open");
	}
	
	
	
	
}
